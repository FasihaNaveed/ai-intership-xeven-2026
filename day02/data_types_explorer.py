"""
This script demonstrates Python data types:
int, float, bool, str with type conversion examples
"""

# Integer
age = 20
print("Age:", age)
print("Type:", type(age))  # int

# Float
height = 5.7
print("Height:", height)
print("Type:", type(height))  # float

# Boolean
is_student = True
print("Is Student:", is_student)
print("Type:", type(is_student))  # bool

# String
name = "Ali"
print("Name:", name)
print("Type:", type(name))  # str

# Type Conversion
num = 10
print("Int to str:", str(num))

text = "20"
print("Str to int:", int(text))

flt = 10.5
print("Float to int:", int(flt))